🎓 STUDENT MANAGEMENT — Recommended Submodules (Student Side)
1. 🧾 Student Profile

Core identity ng student.

Personal Information
Name, gender, birthdate
Address / contact info
Academic Info
Student No.
Course
Year Level
Status (Active, Inactive, Graduating, etc.)
Profile Picture

👉 Purpose: master record ng student

2. 🏫 Enrollment Module

Pinaka-importanteng part.

New Enrollment
Re-Enrollment
Subject selection
Section assignment
School year & semester tracking
Enrollment status (Pending / Enrolled / Dropped)

👉 Tables usually:

enrollments
enrollment_subjects
3. 📚 Subject & Class List

Kung student view:

Enrolled subjects
Class schedule
Day
Time
Room
Instructor
Units per subject

👉 Read-only for students

4. 📊 Grades Module

Academic performance.

Midterm / Final grades
GPA / General Average
Per subject grade breakdown
Remarks (Passed, Failed, INC)

👉 Optional:

Grade history per semester
5. 💰 Billing / Assessment Module

Finance side ng student.

Tuition breakdown
Tuition fee
Misc fees
Other charges
Discounts / Scholarship
Balance
Payment history

👉 Tables:

assessments
payments
6. 💳 Payment Module

Actual transactions.

Payment history
Official receipts (OR number)
Payment method (cash, online, etc.)
Remaining balance
7. 📅 Attendance Module

Optional but powerful.

Per subject attendance
Present / Late / Absent
Attendance percentage
8. 📢 Announcements / Notifications

System-wide communication.

School announcements
Class announcements
Enrollment reminders
Deadline alerts
9. 📄 Documents Module

Requests & records.

Certificate of enrollment
Certificate of grades
Transcript requests
Clearance
10. ⚙️ Account Settings

Student account control.

Change password
Update profile
Login history (optional)

